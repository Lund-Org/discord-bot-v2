export function generateName() {
  const dungeon = dungeonName[Math.floor(Math.random() * dungeonName.length)];
  const owner = ownerNames[Math.floor(Math.random() * ownerNames.length)];

  if (['A', 'E', 'Y', 'U', 'I', 'O'].includes(owner[0])) {
    return `${dungeon} d'${owner}`;
  } else {
    return `${dungeon} de ${owner}`;
  }
}
